    // Wrapper am√©lior√© pour cr√©er un graphique en doughnut
    async createDoughnut(canvasId, data, options = {}) {
        try {
            const canvas = await this.prepareCanvas(canvasId);
            if (!canvas) return null;
            
            const ctx = canvas.getContext('2d');
            
            // Pr√©paration des datasets
            const preparedData = {
                ...data,
                datasets: this.prepareDatasets(data.datasets, 'doughnut')
            };
            
            const config = {
                type: 'doughnut',
                data: preparedData,
                options: {
                    ...this.defaultConfig,
                    cutout: '65%',
                    plugins: {
                        ...this.defaultConfig.plugins,
                        legend: {
                            ...this.defaultConfig.plugins.legend,
                            position: 'bottom',
                            labels: {
                                ...this.defaultConfig.plugins.legend.labels,
                                generateLabels: function(chart) {
                                    const data = chart.data;
                                    if (data.labels.length && data.datasets.length) {
                                        return data.labels.map((label, i) => {
                                            const dataset = data.datasets[0];
                                            const value = dataset.data[i];
                                            const total = dataset.data.reduce((sum, val) => sum + val, 0);
                                            const percentage = ((value / total) * 100).toFixed(1);
                                            
                                            return {
                                                text: `${label} (${percentage}%)`,
                                                fillStyle: Array.isArray(dataset.backgroundColor) 
                                                    ? dataset.backgroundColor[i] 
                                                    : dataset.backgroundColor,
                                                strokeStyle: dataset.borderColor,
                                                lineWidth: dataset.borderWidth,
                                                hidden: isNaN(value) || value === 0,
                                                index: i
                                            };
                                        });
                                    }
                                    return [];
                                }
                            }
                        }
                    },
                    ...options
                }
            };
            
            const chart = new Chart(ctx, config);
            this.charts.set(canvasId, chart);
            console.log(`üç© Graphique en doughnut ${canvasId} cr√©√© avec succ√®s`);
            return chart;
            
        } catch (error) {
            console.error(`‚ùå Erreur lors de la cr√©ation du graphique en doughnut ${canvasId}:`, error);
            return null;
        }
    }
    
