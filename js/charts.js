    // Graphique en donut optimis√© - r√©partition des √©tats d'avancement
    async createEtatDonutChart(canvasId, labels, data, options = {}) {
        if (!labels || !data || labels.length !== data.length) {
            console.error('Donn√©es invalides pour createEtatDonutChart');
            return null;
        }

        // G√©n√©ration des couleurs bas√©es sur le mapping des √©tats
        const colors = labels.map((label, index) => {
            const labelClean = label?.toString().trim();
            return this.colors.stateColors[labelClean] || 
                   this.colors.chartColors[index % this.colors.chartColors.length];
        });
        
        try {
            const canvas = await this.prepareCanvas(canvasId);
            if (!canvas) return null;
            
            const ctx = canvas.getContext('2d');
            
            const config = {
                type: 'doughnut',
                data: {
                    labels: labels,
                    datasets: [{
                        data: data,
                        backgroundColor: colors,
                        borderColor: '#FFFFFF',
                        borderWidth: 3,
                        hoverBorderWidth: 5,
                        hoverBackgroundColor: colors.map(color => color + 'DD')
                    }]
                },
                options: {
                    ...this.defaultConfig,
                    cutout: '70%',
                    plugins: {
                        ...this.defaultConfig.plugins,
                        legend: {
                            ...this.defaultConfig.plugins.legend,
                            position: 'bottom',
                            labels: {
                                ...this.defaultConfig.plugins.legend.labels,
                                padding: 25,
                                usePointStyle: true,
                                pointStyle: 'circle',
                                generateLabels: function(chart) {
                                    const data = chart.data;
                                    if (data.labels.length && data.datasets.length) {
                                        const dataset = data.datasets[0];
                                        const total = dataset.data.reduce((sum, val) => sum + val, 0);
                                        
                                        return data.labels.map((label, i) => {
                                            const value = dataset.data[i];
                                            const percentage = total > 0 ? ((value / total) * 100).toFixed(1) : '0.0';
                                            
                                            return {
                                                text: `${label}: ${value} (${percentage}%)`,
                                                fillStyle: Array.isArray(dataset.backgroundColor) 
                                                    ? dataset.backgroundColor[i] 
                                                    : dataset.backgroundColor,
                                                strokeStyle: dataset.borderColor,
                                                lineWidth: dataset.borderWidth,
                                                hidden: isNaN(value) || value === 0,
                                                index: i
                                            };
                                        });
                                    }
                                    return [];
                                }
                            }
                        },
                        tooltip: {
                            ...this.defaultConfig.plugins.tooltip,
                            callbacks: {
                                title: function(context) {
                                    return context[0].label;
                                },
                                label: function(context) {
                                    const label = context.label || '';
                                    const value = context.raw;
                                    const total = context.dataset.data.reduce((sum, val) => sum + val, 0);
                                    const percentage = total > 0 ? ((value / total) * 100).toFixed(1) : '0.0';
                                    return `${label}: ${value} communes (${percentage}%)`;
                                }
                            }
                        }
                    },
                    layout: {
                        padding: 20
                    },
                    ...options
                }
            };
            
            const chart = new Chart(ctx, config);
            this.charts.set(canvasId, chart);
            console.log(`üç© Graphique √©tat donut ${canvasId} cr√©√© avec succ√®s`);
            return chart;
            
        } catch (error) {
            console.error(`‚ùå Erreur lors de la cr√©ation du graphique √©tat donut ${canvasId}:`, error);
            return null;
        }
    }

    // M√©thode de nettoyage pour navigation entre sections
    cleanupForNavigation() {
        console.log('üßπ Nettoyage des graphiques pour navigation...');
        this.destroyAll();
    }
    
    // Redimensionnement pour responsive
    resize() {
        this.charts.forEach((chart, chartId) => {
            try {
                if (chart && typeof chart.resize === 'function') {
                    chart.resize();
                }
            } catch (error) {
                console.warn(`‚ö†Ô∏è Erreur lors du redimensionnement du graphique ${chartId}:`, error);
            }
        });
    }
    
    // M√©thodes utilitaires inchang√©es
    getColor(index) {
        return this.colors.chartColors[index % this.colors.chartColors.length];
    }
    
    getColors(count) {
        return Array.from({length: count}, (_, i) => this.getColor(i));
    }
    
    getStateColor(state) {
        const stateClean = state?.toString().trim();
        return this.colors.stateColors[stateClean] || '#6B7280';
    }
